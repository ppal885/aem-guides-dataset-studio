"""Render a small, stdlib-only Python client that POSTs a dataset config to POST /api/v1/jobs.

This gives a ChatGPT-style "here is a script you can run" workflow without executing
model-generated code on the server: the script is produced from a *validated* config dict.
"""

from __future__ import annotations

import base64
import json
from typing import Any


def render_jobs_api_python_script(
    *,
    config: dict[str, Any],
    api_base_url: str = "http://127.0.0.1:8001",
) -> str:
    """Return UTF-8 Python source. Uses urllib only (no pip deps).

    The caller must pass JSON-serializable, validated dataset job config (same shape
    as Builder / POST /api/v1/jobs ``config``).
    """
    base = str(api_base_url or "").strip().rstrip("/") or "http://127.0.0.1:8001"
    raw = json.dumps(config, ensure_ascii=True, separators=(",", ":"), sort_keys=True).encode("utf-8")
    b64 = base64.b64encode(raw).decode("ascii")

    return f'''#!/usr/bin/env python3
# Generated by AEM Guides Studio — safe template (not arbitrary LLM exec).
# Run:  set AEM_STUDIO_URL={base}
#       set AEM_STUDIO_TOKEN=your-bearer-token
#       python this_file.py
#
# Calls POST /api/v1/jobs synchronously; when the job completes, response JSON includes
# download_url / id. Large jobs may take several minutes (timeout is 2 hours below).

from __future__ import annotations

import base64
import json
import os
import ssl
import urllib.error
import urllib.request

API_BASE = os.environ.get("AEM_STUDIO_URL", {base!r}).rstrip("/")
TOKEN = (os.environ.get("AEM_STUDIO_TOKEN") or os.environ.get("API_BEARER_TOKEN") or "").strip()

_CONFIG_B64 = "{b64}"
CONFIG = json.loads(base64.b64decode(_CONFIG_B64).decode("utf-8"))


def main() -> None:
    if not TOKEN:
        print("Set AEM_STUDIO_TOKEN (or API_BEARER_TOKEN) to your API bearer token.", flush=True)
        raise SystemExit(2)
    url = f"{{API_BASE}}/api/v1/jobs"
    payload = json.dumps({{"config": CONFIG}}).encode("utf-8")
    req = urllib.request.Request(
        url,
        data=payload,
        headers={{
            "Content-Type": "application/json",
            "Authorization": f"Bearer {{TOKEN}}",
        }},
        method="POST",
    )
    ctx = ssl.create_default_context()
    try:
        with urllib.request.urlopen(req, timeout=7200, context=ctx) as resp:
            out = resp.read().decode("utf-8")
    except urllib.error.HTTPError as exc:
        print(exc.read().decode("utf-8", errors="replace"), flush=True)
        raise SystemExit(exc.code) from exc
    print(out, flush=True)
    try:
        data = json.loads(out)
    except json.JSONDecodeError:
        return
    jid = data.get("id")
    if jid:
        print(f"\\nJob id: {{jid}}", flush=True)
    du = data.get("download_url")
    if du:
        print(f"Download path (append to API_BASE): {{du}}", flush=True)


if __name__ == "__main__":
    main()
'''
