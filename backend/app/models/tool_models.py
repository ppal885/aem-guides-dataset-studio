"""Tool-calling and planner models for the enterprise QA copilot."""

from __future__ import annotations

from typing import Any

from pydantic import BaseModel, Field


class ExtractedEntities(BaseModel):
    """Structured interpretation of a user query."""

    customer: str | None = None
    feature: str | None = None
    request_type: list[str] = Field(default_factory=list)
    issue_type: str | None = None
    environment: str | None = None
    editor_type: str | None = None
    output_type: str | None = None
    time_window_days: int | None = None
    source_jira_key: str | None = None
    escalation_only: bool = False
    confidence: float = Field(default=0.0, ge=0.0, le=1.0)
    extraction_path: str = "rules"
    notes: list[str] = Field(default_factory=list)


class PlannedToolCall(BaseModel):
    """A planned call generated by the planner agent."""

    sequence: int
    name: str
    arguments: dict[str, Any] = Field(default_factory=dict)
    reason: str = ""
    depends_on: list[str] = Field(default_factory=list)


class PlannerResult(BaseModel):
    """Full output of the planner agent."""

    entities: ExtractedEntities
    tool_calls: list[PlannedToolCall] = Field(default_factory=list)
    fallback_strategy: list[str] = Field(default_factory=list)
    planner_path: str = "rules"


class ToolExecutionRecord(BaseModel):
    """Observable record for one tool execution."""

    name: str
    arguments: dict[str, Any] = Field(default_factory=dict)
    success: bool
    latency_ms: float
    output: Any = None
    error: str | None = None
    metadata: dict[str, Any] = Field(default_factory=dict)
